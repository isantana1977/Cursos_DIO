a = int(input('Qual o valor para a? '))
b = int(input('Qual o valor de b? '))
soma = a + b
subtracao = a - b
multi = a * b
divi = a / b
print ('A SOMA: entre ', a, ' e ', b, ' é igual a ',soma)
print ('A DIFERENÇA entre ', a, ' e ', b, ' é igual a ',subtracao)
print ('O PRODUTO entre ', a, ' e ', b, ' é igual a ',multi)
print ('O QUOCIENTE entre ', a, ' e ', b, ' é igual a ',divi)

print("Soma: {} \nDiferença: {} \nProduto: {} \nQuociente: {}".format(soma, subtracao,multi, divi))